package itrans.newinterface.Nearby;

public class NearbyBusTimings {
    private String busService;
    private String busTiming;
    private String busLoad;

    public String getBusService() {
        return busService;
    }

    public void setBusService(String busService) {
        this.busService = busService;
    }

    public String getBusTiming() {
        return busTiming;
    }

    public void setBusTiming(String busTiming) {
        this.busTiming = busTiming;
    }

    public String getBusLoad() {
        return busLoad;
    }

    public void setBusLoad(String busLoad) {
        this.busLoad = busLoad;
    }
}
